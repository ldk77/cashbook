package vo;

public class Help {
	
	private int helpNo; 
	private String helpMemo;
	private String memberId; 
	private String updatedete;
	private String createdate;
	public int getHelpNo() {
		return helpNo;
	}
	public void setHelpNo(int helpNo) {
		this.helpNo = helpNo;
	}
	public String getHelpMemo() {
		return helpMemo;
	}
	public void setHelpMemo(String helpMemo) {
		this.helpMemo = helpMemo;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getUpdatedete() {
		return updatedete;
	}
	public void setUpdatedete(String updatedete) {
		this.updatedete = updatedete;
	}
	public String getCreatedate() {
		return createdate;
	}
	public void setCreatedate(String createdate) {
		this.createdate = createdate;
	}
	
}
